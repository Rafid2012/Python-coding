a = {1,3,5,7,9}
b = {2,4,6,7,8}
print("Set a: ",a)
print("Set b: ",b)

# union

print("Union: ",a.union(b))

# intersection

print("Intersection:",a.intersection(b))

# difference (a-b)

print("Differance: ",a.difference(b))

#difference (b-a)

print("Difference: ",b.difference(a))

# symmetric difference

print("symmetric difference: ",a.symmetric_difference(b))



